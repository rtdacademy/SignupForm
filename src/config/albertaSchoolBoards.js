export const getAllAuthorities = () => {
  return { ...PUBLIC_AUTHORITIES, ...SEPARATE_AUTHORITIES };
};

/**
 * Searches for cities by name (case-insensitive partial match)
 * @param {string} searchTerm - Search term
 * @returns {Array} Array of matching city names
 */
export const searchCities = (searchTerm) => {
  if (!searchTerm) return [];
  
  const lowerSearchTerm = searchTerm.toLowerCase();
  return Object.keys(ALBERTA_SCHOOL_BOARDS)
    .filter(city => city.toLowerCase().includes(lowerSearchTerm))
    .sort();
};

/**
 * Gets cities served by a specific school authority
 * @param {string} authorityCode - Authority code
 * @returns {Array} Array of cities served by this authority
 */
export const getCitiesForAuthority = (authorityCode) => {
  const cities = [];
  
  Object.entries(ALBERTA_SCHOOL_BOARDS).forEach(([city, data]) => {
    const publicMatch = data.publicAuthority?.code === authorityCode;
    const separateMatch = data.separateAuthority?.code === authorityCode;
    
    if (publicMatch || separateMatch) {
      cities.push(city);
    }
  });
  
  return cities.sort();
};

/**
 * Gets cities that have both public and separate authorities
 * @returns {Array} Array of city names with both authority types
 */
export const getCitiesWithBothAuthorities = () => {
  return Object.entries(ALBERTA_SCHOOL_BOARDS)
    .filter(([city, data]) => data.publicAuthority && data.separateAuthority)
    .map(([city, data]) => city)
    .sort();
};

/**
 * Gets cities that only have public authorities
 * @returns {Array} Array of city names with only public authorities
 */
export const getCitiesWithOnlyPublicAuthorities = () => {
  return Object.entries(ALBERTA_SCHOOL_BOARDS)
    .filter(([city, data]) => data.publicAuthority && !data.separateAuthority)
    .map(([city, data]) => city)
    .sort();
};

/**
 * Gets school authority information by authority name (partial match)
 * @param {string} authorityName - Authority name to search for
 * @returns {Array} Array of matching authorities with codes
 */
export const searchAuthorities = (authorityName) => {
  if (!authorityName) return [];
  
  const lowerSearchTerm = authorityName.toLowerCase();
  const matches = [];
  
  const allAuthorities = getAllAuthorities();
  Object.entries(allAuthorities).forEach(([code, name]) => {
    if (name.toLowerCase().includes(lowerSearchTerm)) {
      matches.push({ code, name });
    }
  });
  
  return matches.sort((a, b) => a.name.localeCompare(b.name));
};

/**
 * Gets statistics about the school board database
 * @returns {Object} Statistics object
 */
export const getStatistics = () => {
  const totalCities = Object.keys(ALBERTA_SCHOOL_BOARDS).length;
  const citiesWithSeparate = getCitiesWithBothAuthorities().length;
  const citiesOnlyPublic = getCitiesWithOnlyPublicAuthorities().length;
  const totalPublicAuthorities = Object.keys(PUBLIC_AUTHORITIES).length;
  const totalSeparateAuthorities = Object.keys(SEPARATE_AUTHORITIES).length;
  
  return {
    totalCities,
    citiesWithSeparate,
    citiesOnlyPublic,
    totalPublicAuthorities,
    totalSeparateAuthorities,
    totalAuthorities: totalPublicAuthorities + totalSeparateAuthorities
  };
};

/**
 * Gets all authorities grouped by type for dropdown components
 * @returns {Object} Grouped authorities
 */
export const getAuthoritiesForDropdown = () => {
  const publicOptions = Object.entries(PUBLIC_AUTHORITIES).map(([code, name]) => ({
    value: code,
    label: name,
    type: 'public'
  }));
  
  const separateOptions = Object.entries(SEPARATE_AUTHORITIES).map(([code, name]) => ({
    value: code,
    label: name,
    type: 'separate'
  }));
  
  return {
    public: publicOptions.sort((a, b) => a.label.localeCompare(b.label)),
    separate: separateOptions.sort((a, b) => a.label.localeCompare(b.label)),
    all: [...publicOptions, ...separateOptions].sort((a, b) => a.label.localeCompare(b.label))
  };
};

/**
 * Gets cities formatted for dropdown components
 * @returns {Array} Array of city options for dropdowns
 */
export const getCitiesForDropdown = () => {
  return Object.keys(ALBERTA_SCHOOL_BOARDS)
    .sort()
    .map(city => ({
      value: city,
      label: city
    }));
};

